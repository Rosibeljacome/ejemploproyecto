<div> <b> <center>  Tipos de comida <center> </b> </div>
<br>

Existe todo tipo de cocina en el mundo, cada país tiene un plato típico en específico y en él deja un poco de su cultura. Entre las mas destacadas estan la comida china, la venezolana, italiana, libanesa, japonesa, vietnamita, vegetariana, peruana, mexicana, hindú y vegana. La gastronomía ha logrado que los platos de todos esos países se puedan encontrar a nivel mundial, incluso se pueden preparar en casa o simplemente llevar la comida a domicilio.
<br>
<br>
<b> Comidas típicas por región</b> 
Cada país tiene un plato típico, por ejemplo, la comida china se caracteriza por tener platos picantes y muy extensos. No se trata de una sola comida que representa a todo el país, sino a varios platillos cuyo origen es de una región en específico y sus ingredientes varían en torno a eso.
<br>
<br>
El cerdo agridulce es una de las especialidades chinas, tal como los rollitos de primavera y los dumplings. Ahora, respecto a la italiana, se trata más que todo de pastas y la famosa pizza. En los juegos de comida online se pueden encontrar la mayoría de estos platos gastronómicos.
<br>
<br>
<b> Comida vegetariana</b> 
Se caracterizan por tener alimentos ricos en nutrientes. Todas las comidas vegetarianas están compuestas por vegetales o, en su defecto, por frutas, excluyendo al 100% la carne por el simple hecho de tener origen animal. Los vegetarianos del occidente del mundo se permiten consumir huevos, leche e incluso queso, sin embargo, las carnes, bien sean rojas o blancas, están totalmente prohibidas.

Existen muchos platos de comida vegetariana tomadas en cuenta como recetas de comida saludable, desde los cereales hasta las llamadas ensaladas de frutas, tofu, ensalada de vegetales, etc. Los postres vegetarianos son preparados con frutas frescas.
<br>
<br>
<b> Comida vegana</b> 
Muchos creen que ser vegano y vegetariano es exactamente lo mismo y la verdad es que no, ambas pueden tener similitudes pero hay una característica en específico que las diferencia totalmente. Mientras que los vegetarianos aceptan consumir leche, queso y huevos, los veganos no. Ellos consumen únicamente alimentos como las frutas y los vegetales, dejando a un lado totalmente los productos de origen animal.
<br>
<br>
<b> Comida chatarra</b> 
Este tipo de gastronomía es bastante común en el mundo debido a lo fácil y rápido que puede llegar a ser su preparación, además, se caracteriza por contener ingredientes con demasiadas grasas, azúcares y condimentos y aunque su sabor es inigualable, su consumo habitual puede llegar a causar enfermedades severas en las personas.
<br>
<br>
Las hamburguesas, los hot dogs, pizzas, nachos con cremas, etc, son algunos de los ejemplos de esta clase de comida.          




