La gastronomía es importante en cada rincón del mundo debido a que cada plato de comida representa la cultura de los pueblos, ciudades y países sin importar que éstos sean grandes o pequeños. La comida no solo transmite los sabores combinados en cada ingrediente, sino que, indirectamente, proporciona conocimientos sobre el estilo de vida de las personas de una región determinada, sus costumbres, su cultura.<br><br>

