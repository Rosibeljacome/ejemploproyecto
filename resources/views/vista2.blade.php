<b> <center>  Elementos de la comida</b> <center> </div>
<br>
<br>

Estos elementos no son más que los nutrientes que están relacionados a cada alimento que se consume a diario. entre ellos se encuentran:
<br>
<br>

<b>Carbohidratos</b>
Son conocidos por tener un sabor muy dulce y tienen como función primordial dotar de energía a toda la anatomía humana. Originalmente los hidratos de carbono se encuentran en legumbres, frutas, verduras, etc. Aunque también están muy presentes en las comidas y alimentos preparados como en refrescos, dulces, chucherías, etc.
<br>
<br>
<b>Proteínas</b>
Se trata de al menos 20 aminoácidos que se unen para formar lo que se conoce como proteínas, aquellas que actúan en el organismo para lograr la regeneración muscular y de los tejidos.
<br>
<br>
<b>Grasas</b>
Estas le brindan al cuerpo mucha energía, pero cuando hay demasiada grasa en el organismo, se tienden a padecer anomalías o enfermedades.
<br>
Las grasas tienen una clasificación importante que denota su tipo y correcta ubicación. Las saturadas nacen en los animales, las monoinsaturadas se encuentran en las aceitunas, aceite de oliva y los aguacates o paltas y, por último, las poliinsaturadas que se encuentran en las semillas y aceites (mejor conocidas como el omega 3). Las grasas, en cualquiera de sus tipos, debe ser mínima en la comida para diabéticos.
<br>
<br>
<b>Lípidos</b>
Tal como lo hacen las grasas y carbohidratos, los lípidos hacen que el cuerpo esté enérgico durante el día, sin embargo, también tienen otra función y es la de proporcionar una temperatura adecuada al cuerpo, así como también mantenerla regulada.
<br>
<br>
<b>Sales minerales</b>
Se trata de las sales neutras que mantienen en completo control al metabolismo. Se conoce como sal mineral a la sal de mesa que se emplea frecuentemente en la comida, de hecho, si no se utiliza, se tiende a decir que el plato culinario resultó ser muy insípido.
<br>
<br>
<b>Vitaminas</b>
Estos nutrientes no son generados por el organismo, por lo tanto, se deben consumir mediante los alimentos diarios. Son imperativas en el organismo porque en su ausencia, el sujeto se debilita y padece problemas metabólicos serios.
<br>
<br>
<b>Compuestos inorgánicos</b>
Se trata de compuestos o elementos inorgánicos que el cuerpo no puede producir, sin embargo, se encuentran en alimentos, suplementos alimenticios y medicamentos especiales. Los minerales, el agua y el nitrógeno forman parte de estos compuestos.
<br>
<br>
<b>Minerales</b>
Son elementos de suma importancia para el organismo, no solo a nivel metabólico sino también estructural, pues algunos minerales funcionan en los huesos, por ejemplo, el calcio.

